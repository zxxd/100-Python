#学习使用turtle在屏幕上绘制图形，很想问问这个包为什么要叫turtle

import turtle

turtle.pensize(4)
turtle.pencolor('red')
turtle.forward(100)
turtle.right(90)
turtle.forward(100)
turtle.right(90)
turtle.forward(100)
turtle.right(90)
turtle.forward(100)
turtle.mainloop()

#forward是前进的意思，整部代码为前进100，向右90度，前进100，向右90度，循环往复形成一个正方形
